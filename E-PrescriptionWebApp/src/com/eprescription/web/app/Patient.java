package com.eprescription.web.app;

public class Patient {
	
	private String firstName;
	private String lastName;
	private String phoneNumber;
	private String street;
	private String city;
	private int zipCode;
	private String state;
	private String sex;
	private String dateOfBirth;
	
	public Patient(){
		super();
	}
	
	public Patient(Object obj){
		Patient newPatient = (Patient) obj;
		this.firstName = newPatient.getFirstName();
		this.lastName = newPatient.getLastName();
		this.phoneNumber = newPatient.getPhoneNumber();
		this.street = newPatient.getStreet();
		this.city = newPatient.getCity();
		this.zipCode = newPatient.getZipCode();
		this.state = newPatient.getState();
		this.sex = newPatient.getSex();
		this.dateOfBirth = newPatient.getDateOfBirth();
	}

	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}

	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	/**
	 * @return the lastName
	 */
	public String getLastName() {
		return lastName;
	}

	/**
	 * @param lastName the lastName to set
	 */
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	
	/**
	 * @return the full name of the patient
	 */
	public String getFullName() {
		return firstName + " " + lastName;
	}

	/**
	 * @return the phoneNumber
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}

	/**
	 * @param phoneNumber the phoneNumber to set
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	/**
	 * @return the street
	 */
	public String getStreet() {
		return street;
	}

	/**
	 * @param street the street to set
	 */
	public void setStreet(String street) {
		this.street = street;
	}

	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}

	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}

	/**
	 * @return the zipCode
	 */
	public int getZipCode() {
		return zipCode;
	}

	/**
	 * @param zipCode the zipCode to set
	 */
	public void setZipCode(int zipCode) {
		this.zipCode = zipCode;
	}

	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}

	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	
	/**
	 * @return the full address
	 */
	public String getAddres() {
		return street + ", " + city + ", " + state + " " + zipCode;
	}

	/**
	 * @return the sex
	 */
	public String getSex() {
		return sex;
	}

	/**
	 * @param sex the sex to set
	 */
	public void setSex(String sex) {
		this.sex = sex;
	}

	/**
	 * @return the dateOfBirth
	 */
	public String getDateOfBirth() {
		return dateOfBirth;
	}

	/**
	 * @param dateOfBirth the dateOfBirth to set
	 */
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}

}
